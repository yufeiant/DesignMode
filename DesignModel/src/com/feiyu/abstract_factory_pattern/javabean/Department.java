package com.feiyu.abstract_factory_pattern.javabean;

public class Department {
	private String name;
	private int ount;
	private int id;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getOunt() {
		return ount;
	}
	public void setOunt(int ount) {
		this.ount = ount;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
}
